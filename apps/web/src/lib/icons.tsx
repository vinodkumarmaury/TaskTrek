// Helper function to get icon component
export function getIcon(emoji: string, className?: string): React.ReactElement | null {
  const IconComponent = iconMap[emoji];
  if (!IconComponent) {
    console.warn(`No icon mapping found for emoji: ${emoji}`);
    return null;
  }
  
  return <IconComponent className={className} />;
}

